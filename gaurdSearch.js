let currentDir = "up";
let currentLocation = null;

// find current location
for (let y = 0; y < map.length; y++) {
  for (let x = 0; x < map[y].length; x++) {
    if (map[y][x] === "^") {
      currentLocation = [x, y];
      break;
    }
  }
}
hasBeen[currentLocation[0]][currentLocation[1]] = 1

const move = () => {
  const x = currentLocation[0];
  const y = currentLocation[1];
  if (currentDir === "up") {
    if (y - 1 < 0) return null;
    if (map[y - 1][x] === "#") {
      currentDir = "right";
      return move();
    }
    return [x, y - 1];
  }

  if (currentDir === "right") {
    if (x + 1 >= map[y].length) return null;
    if (map[y][x + 1] === "#") {
      currentDir = "down";
      return move();
    }
    return [x + 1, y];
  }

  if (currentDir === "down") {
    if (y + 1 >= map.length) return null;
    if (map[y][x + 1] === "#") {
      currentDir = "left";
      return move();
    }
    return [x, y + 1];
  }

  if (currentDir === "left") {
    if (x - 1 < 0) return null;
    if (map[y][x - 1] === "#") {
      currentDir = "up";
      return move();
    }
    return [x - 1, y];
  }
};

count = 1;
while (currentLocation !== null) {
  currentLocation = move();
  if (currentLocation !== null) {
    if (hasBeen[currentLocation[0]][currentLocation[1]] !== 1) {
        count++;
        hasBeen[currentLocation[0]][currentLocation[1]] = 1
    }
  }
}

